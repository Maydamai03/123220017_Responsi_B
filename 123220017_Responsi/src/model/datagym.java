public class datagym {
    private String nama;
    private String alat;
    private String telepon;
    private Integer sewa;
    private Integer totalbiaya;

    public String getNama() {
        return nama;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }

    public String getAlat() {
        return alat;
    }

    public void setAlat(String alat) {
        this.alat = alat;
    }

    public String getTelepon() {
        return telepon;
    }

    public void setTelepon(String telepon) {
        this.telepon = telepon;
    }

    public Integer getSewa() {
        return sewa;
    }

    public void setSewa(Integer sewa) {
        this.sewa = sewa;
    }

    public Integer getTotalbiaya() {
        return totalbiaya;
    }

    public void setTotalbiaya(Integer totalbiaya) {
        this.totalbiaya = totalbiaya;
    }
    
    
}
